public class MaisVelho {

    private String nome;
    private int idade;

    // Constructor
    public MaisVelho(String nome, int idade){
        this.nome = nome;
        this.idade = idade;
    }

    // Getters & Setters
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
}
